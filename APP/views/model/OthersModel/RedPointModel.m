    return @"UUID";
}

- (id)init
{
    self = [super init];
    if (self) {
        self.hadMyConsult = NO;
        self.hadOtherConsult = NO;
        self.hadWaiting = NO;
        self.hadMessage = NO;
        self.hadNotice = NO;
        
        self.numMyConsult = 0;
        self.numOtherConsult = 0;
        self.numWaiting = 0;
        self.numNotice = 0;
    }
    return self;
}

